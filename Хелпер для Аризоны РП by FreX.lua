function main()
	if not isSampLoaded() or not isSampfuncsLoaded() then return end
	while not isSampAvailable() do wait (100) end
	sampRegisterChatCommand('fk', cmd_fk)
	sampRegisterChatCommand('fx', cmd_fx)
	sampRegisterChatCommand('fc', cmd_fc)
	sampRegisterChatCommand('f', cmd_f)
	
	sampAddChatMessage(tag, main_color)
	
	while true do
	wait(0)
        if isKeyDown (VK_CONTROL) and isKeyJustPressed (VK_O) then -- /time
            sampSendChat('/time')
	    end
		
		 if isKeyDown (VK_CONTROL) and isKeyJustPressed (VK_Z) then -- автопиар фамы
            sampSendChat('/vr Проходит набор в семью Ibrahimov. Дружный коллектив ждём всех.')
	    end
		
		if isKeyJustPressed (VK_X) then -- открывает дверь
            sampSendChat('/opengate')
	    end
		if isKeyJustPressed(VK_NUMPAD3) then
			sampSendChat('/newsredak') --Улучшенная ловля объявлений
			setVirtualKeyDown(0x0D, true)
			wait(110)
			setVirtualKeyDown(0x0D, false)
		end
		if isKeyJustPressed(VK_NUMPAD2) then
			sampSendChat('/newsredak') --Улучшенная ловля объявлений
		end
	end
end

